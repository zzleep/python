import math # Math Module

pi = 3.14
x = 1
y = 2
z = 3

print(round(pi)) # Rounding a number
print(math.ceil(pi)) # Ceil in short for ceiling and will round the integer to the nearest whole number
print(math.floor(pi)) # Rounding down
print(abs(pi)) # Absolute value of a number, will tell you how far the number is from zero
print(pow(pi,2)) # Raising pi to the power of 2
print(math.sqrt(pi)) # Square root of a value
print(max(x,y,z)) # Finding the largest value between variables
print(min(x,y,z)) # Finding the lowest value

