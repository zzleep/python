# 2D lists = a list of lists

drinks = ["coffee", "soda", "tea"]
dinner = ["pizza", "hamburger", "hotdog"]
dessert = ["cake", "ice cream"]

food = [drinks, dinner, dessert]

print(food[0][2]) # 0 represents selection of the list drinks within the food variable and the second one indicates
                  # the index selection within the list selected

