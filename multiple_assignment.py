# Multiple Assignment allows us to assign multiple variables at the same time in one line of code

# name = "Jackson"
# age = 23
# attractive = True

name, age, attractive = "Jackson", 23, True
# Acts as one line of code!!

print(name)
print(age)
print(attractive)

