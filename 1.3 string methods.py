# String Methods

name = "jidey"

print(len(name)) # Prints out the length of the string
print(name.find("d")) # Finds the character within a string in the form of indices
print(name.capitalize()) # Will only capitalize first letter of the string
print(name.upper()) # Will make all letters uppercase
print(name.lower()) # Will make all letters lowercase
print(name.isdigit()) # Verifies if the variable contains a number or not
print(name.isalpha()) # Checks if string only contains alphabetical letters
print(name.count("e")) # Counts how many iterations the character has
print(name.replace("i", "o")) # Replaces original value to a different value
print(name*3) # Prints the variable with N number of times
